#!/usr/bin/env python
# -*- coding: utf-8 -*-

"""
Este script convertirá un valor en decimal a binario e imprimirá el resultado
"""

# Se crea una variable con el valor decimal
valor_decimal = 255

# Se convierte el valor a binario usando la función bin() y se guarda el valor
# en otra variable.
valor_binario = bin(valor_decimal)

# Se imprime el valor decimal
print("Valor en decimal:", valor_decimal)

# Se imprime el valor binario
print("Valor en binario:", valor_binario)